    private void btnGrabarModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarModActionPerformed
        try{    
            Estudiante e = arrEstudiantes.buscar(getCodigo());
            if(e != null){
                e.setNombre(getNombre());
                e.setCiclo(getCiclo());
                e.setPension(getPension());

                arrEstudiantes.grabar();
                listar();
            }
            limpiaCajas();
            habilitaCajas(false);

            btnModificar.setVisible(true);
            btnGrabarMod.setVisible(false);
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la aplicación: " + e);
        }
    }//GEN-LAST:event_btnGrabarModActionPerformed

    private void btnGrabarIngActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarIngActionPerformed
        try{    
            Estudiante e = arrEstudiantes.buscar(getCodigo());
            if(e == null){
                e = new Estudiante(getCodigo(), getNombre(), getCiclo(), getPension());

                arrEstudiantes.adicionar(e);
                arrEstudiantes.grabar();
                listar();
            }
            limpiaCajas();
            habilitaCajas(false);

            btnIngresar.setVisible(true);
            btnGrabarIng.setVisible(false);
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la aplicación: " + e);
        }
    }//GEN-LAST:event_btnGrabarIngActionPerformed
    
    
    
    
    
    
