    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            if(valida().equals("")){
                String candidata = getNombre();
                double puntaje1 = getPuntaje1();
                double puntaje2 = getPuntaje2();
                double puntaje3 = getPuntaje3();
                
                arrCandidata.registraCandidata(candidata, calculaPromedio());
                imprimir();
                limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "Error en: \n" + valida());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error en la aplicaci√≥n");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

