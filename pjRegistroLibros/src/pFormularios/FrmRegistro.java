    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try{
            objLibro = new Libro(getLibro(),getEditorial(),getClase(),getAnio(),getPaginas(),getCosto());
            llenaModelos();
            objLibro.masReciente();
            objLibro.menorPaginas();
            objLibro.mayorCosto();
            
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error en la aplicación: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEstadisticasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEstadisticasActionPerformed
        try{
            modEstadisticas.clear();
            llenaEstadisticas();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error en la aplicación: " + ex.getMessage());
            
        }
    }//GEN-LAST:event_btnEstadisticasActionPerformed

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarActionPerformed
        txtLibro.setText("");
        
        cboClase.setSelectedIndex(0);
        cboEditorial.setSelectedIndex(0);
        
        txtAnio.setText("");
        txtPaginas.setText("");
        txtCosto.setText("");
        
        modLibro.clear();
        modEditorial.clear(); 
        modClase.clear(); 
        modAnio.clear(); 
        modPaginas.clear(); 
        modCosto.clear();
        
        Libro.limpiarEstaticos();
        
        modEstadisticas.clear();
        
        txtLibro.requestFocus();
    }//GEN-LAST:event_btnLimpiarActionPerformed

