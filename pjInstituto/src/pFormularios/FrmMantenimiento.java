    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            limpiaCajas();
            limpiaMatriz();
            
            int codigo = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingrese código a buscar:"));
            
            Estudiante e = arrEstudiantes.buscar(codigo);
            
            if(e != null){
                tAlumnos.setValueAt(e.getCodigo(), 0, 0);
                tAlumnos.setValueAt(e.getNombre(), 0, 1);
                tAlumnos.setValueAt(e.getCiclo(), 0, 2);
                tAlumnos.setValueAt(e.getPension(), 0, 3);
            }else{
                JOptionPane.showMessageDialog(null, "Código de estudiantes NO existe");
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de datos");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        int codigo = autogeneraCodigo();
        txtCodigo.setText(""+codigo);
        habilitaCajas(true);
        txtCodigo.setEditable(false);
        txtNombres.requestFocus();
        btnGrabarIng.setVisible(true);
        btnIngresar.setVisible(false);
    }//GEN-LAST:event_btnIngresarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int codigo = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingrese código de estudiante a modificar:"));
        
        habilitaCajas(true);
        limpiaMatriz();
        txtCodigo.setEditable(false);
        
        Estudiante e = arrEstudiantes.buscar(codigo);
        
        if(e != null){
            txtCodigo.setText("" + e.getCodigo());
            txtNombres.setText(e.getNombre());
            txtPension.setText(""+e.getPension());
            cboCiclo.setSelectedIndex(e.getCiclo()-1);
            
            btnGrabarMod.setVisible(true);
            btnModificar.setVisible(false);
            
        }else{
            JOptionPane.showMessageDialog(null, "Código de estudiantes NO existe");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            limpiaCajas();
            limpiaMatriz();
            
            int codigo = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingrese código a eliminar:"));
            
            Estudiante e = arrEstudiantes.buscar(codigo);
            
            if(e != null){
                arrEstudiantes.eliminar(e);
                arrEstudiantes.grabar();
                listar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Código de estudiante no existe");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

