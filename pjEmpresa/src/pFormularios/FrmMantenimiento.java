    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            if (valida().equals("")) {
                Empleado objEmpleado = new Empleado(getCodigo(), getEmpleado(), getHoras(), getTarifa());
                
                arrEmple.adiciona(objEmpleado);
                limpiarControles();
                listar();
                
            } else {
                JOptionPane.showMessageDialog(null, "Error en: \n" + valida());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la aplicación!");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            limpiaTabla();
            
            int codigo = Integer.parseInt(JOptionPane.showInputDialog(null,"Ingrese código a buscar"));
            
            int pos = arrEmple.buscar(codigo);
            
            if(pos == -1)
                JOptionPane.showMessageDialog(null, "Código no existe");
            else{
                tEmpleados.setValueAt(arrEmple.devolver(pos).getCodigo(), 0, 0);
                tEmpleados.setValueAt(arrEmple.devolver(pos).getNombre(), 0, 1);
                tEmpleados.setValueAt(arrEmple.devolver(pos).getHoras(), 0, 2);
                tEmpleados.setValueAt(arrEmple.devolver(pos).getTarifa(), 0, 3);
                tEmpleados.setValueAt(arrEmple.devolver(pos).getTarifa() * 
                                  arrEmple.devolver(pos).getHoras(), 0, 4);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la aplicación!");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

