    void cargaModelos(){
        modLibro = new DefaultListModel();
        modEditorial = new DefaultListModel(); 
        modClase = new DefaultListModel(); 
        modAnio = new DefaultListModel(); 
        modPaginas = new DefaultListModel(); 
        modCosto = new DefaultListModel();
        
        modEstadisticas = new DefaultListModel();
        
        lstLibro.setModel(modLibro);
        lstEditorial.setModel(modEditorial);
        lstClase.setModel(modClase);
        lstAnio.setModel(modAnio);
        lstPaginas.setModel(modPaginas);
        lstCosto.setModel(modCosto);
        
        lstEstadisticas.setModel(modEstadisticas);
    
    }
    
    void llenaModelos(){
        modLibro.addElement(objLibro.getNombre());
        modEditorial.addElement(objLibro.getEditorial()); 
        modClase.addElement(objLibro.getClase()); 
        modAnio.addElement(objLibro.getAnio()); 
        modPaginas.addElement(objLibro.getPaginas()); 
        modCosto.addElement(objLibro.getCosto());
    }
    
    void llenaEstadisticas(){
        modEstadisticas.addElement("Número de libros de Análisis de edición B: " + Libro.gettAnalisisB());
        modEstadisticas.addElement("Libro de edición más reciente: " + Libro.getEdicionMasReciente());
        modEstadisticas.addElement("Editorial con libro de menor páginas: " + Libro.getEditorialMenorPaginas());
        modEstadisticas.addElement("Libro con mayor costo: " + Libro.getLibroMayorCosto());
    }
    
    
    void llenaEditorial(){
        cboEditorial.addItem("A");
        cboEditorial.addItem("B");
        cboEditorial.addItem("C");
    }
    
    void llenaClase(){
        cboClase.addItem("Programación");
        cboClase.addItem("Análisis");
        cboClase.addItem("Diseño");
    }

