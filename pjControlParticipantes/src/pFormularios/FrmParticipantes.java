    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        if(valida().equals("")){
            Participantes objParticipantes = new Participantes();
            
            //Enviando informaci√≥n a la clase
            objParticipantes.setEdad(Integer.parseInt(txtEdad.getText()));
            if(chkMasculino.isSelected()){
                objParticipantes.setSexo("Masculino");
            }else{
                objParticipantes.setSexo("Femenino");
            }
            
            if(rbSoltero.isSelected()){
                objParticipantes.setEstadoCivil("Soltero");
            } else if(rbCasado.isSelected()){
                objParticipantes.setEstadoCivil("Casado");
            } else if(rbViudo.isSelected()){
                objParticipantes.setEstadoCivil("Viudo");
            } else {
                objParticipantes.setEstadoCivil("Divorciado");
            }
            
            //Invocando los conteos
            objParticipantes.determinaEdad();
            objParticipantes.determinaSexo();
            objParticipantes.determinaEstado();
            
            //Obtener los resultados
            int total = objParticipantes.getTotal();
            int totalMayor = Participantes.gettMayores();
            int totalMenor = Participantes.gettMenores();
            int totalHombres = Participantes.gettHombres();
            int totalMujeres = Participantes.gettMujeres();
            int totalSolteros = Participantes.gettSolteros();
            int totalCasados = Participantes.gettCasados();
            int totalViudos = Participantes.gettViudos();
            int totalDivorciados = Participantes.gettDivorciados();
            int porcentajeHombres = (totalHombres * 100) / total;
            int porcentajeMujeres = (totalMujeres * 100) / total;
            
            //Mostrar los resultados
            modResumen.clear();
            modResumen.addElement("** ESTAD√çSTICAS **");
            modResumen.addElement("El total de asistentes es: " + total);
            modResumen.addElement("El total de personas mayores es: " + totalMayor);
            modResumen.addElement("El total de personas menores es: " + totalMenor);
            modResumen.addElement("-----------------------------");
            modResumen.addElement("El total de hombres es: " + totalHombres);
            modResumen.addElement("El total de mujeres es: " + totalMujeres);
            modResumen.addElement("-----------------------------");
            modResumen.addElement("El total de solteros es: " + totalSolteros);
            modResumen.addElement("El total de casados es: " + totalCasados);
            modResumen.addElement("El total de viudos es: " + totalViudos);
            modResumen.addElement("El total de divorciados es: " + totalDivorciados);
            modResumen.addElement("-----------------------------");
            modResumen.addElement("% de hombres: " + porcentajeHombres);
            modResumen.addElement("% de mujeres: " + porcentajeMujeres);
            
            lstResumen.setModel(modResumen);
            
        
        }else{
            JOptionPane.showMessageDialog(null, "El problema es: \n" + valida());
        }
    }//GEN-LAST:event_btnProcesarActionPerformed

