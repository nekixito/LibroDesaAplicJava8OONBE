    private void cboCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCategoriaActionPerformed
        String categoria = String.valueOf(cboCategoria.getSelectedItem());
        double costo = objPago.calculaCosto.calcular(categoria);
        lblCosto.setText(String.format("%.2f", costo));
    }//GEN-LAST:event_cboCategoriaActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        //Capturar los valores del formularios
        String empleado = txtEmpleado.getText();
        String categoria = String.valueOf(cboCategoria.getSelectedItem());
        int horas = Integer.parseInt(txtHoras.getText());
        
        //Obtener los valores resultates
        double costo = objPago.calculaCosto.calcular(categoria);
        double salario = objPago.calculaSalario.calcular(horas, costo);
        double descuento = objPago.calculaDescuento.calcular(salario);
        double neto = objPago.calculaNeto.calcular(salario, descuento);
        
        
        //Imprimir los valores resultantes
        modResumen.addElement("EMPLEADO: " + empleado);
        modResumen.addElement("CATEGOR√çA: " + categoria);
        modResumen.addElement("HORAS TRABAJADAS: " + horas);
        modResumen.addElement("---------------------------");
        modResumen.addElement("COSTO POR HORA: $" + String.format("%.2f", costo));
        modResumen.addElement("MONTO SALARIO: $" + String.format("%.2f", salario));
        modResumen.addElement("MONTO DESCUENTO: $" + String.format("%.2f", descuento));
        modResumen.addElement("MONTO NETO: $" + String.format("%.2f", neto));
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

