    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            
            int codigoB = Integer.parseInt(JOptionPane.showInputDialog(null,"Ingrese código a modificar"));
            
            int pos = arrEmple.buscar(codigoB);
            
            if (pos != -1) {
                Empleado x = arrEmple.devolver(pos);
                btnGrabar.setVisible(true);
                btnModificar.setVisible(false);
                
                txtCodigo.setText("" + codigoB);
                txtCodigo.setEditable(false);
                
                txtEmpleado.setText(x.getNombre());
                txtHoras.setText(""+x.getHoras());
                txtTarifa.setText(""+x.getTarifa());
                txtEmpleado.requestFocus();
                
            } else {
                JOptionPane.showMessageDialog(null, "Error en: \n" + valida());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la aplicación!");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try {
            int pos = arrEmple.buscar(getCodigo());
            
            Empleado x = arrEmple.devolver(pos);
            
            x.setNombre(getEmpleado());
            x.setHoras(getHoras());
            x.setTarifa(getTarifa());
            JOptionPane.showMessageDialog(null, "Empleado actualizado correctamente",
                                        "Confirmación",JOptionPane.INFORMATION_MESSAGE);
            btnModificar.setVisible(true);
            btnGrabar.setVisible(false);
            listar();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar...!","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

