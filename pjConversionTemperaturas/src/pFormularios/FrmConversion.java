    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        double celsius = Double.parseDouble(txtCelsius.getText());
        
        Temperatura objT = new Temperatura();
        
        objT.setCelsius(celsius);
        
        double farenheit = objT.determinaFarenheit();
        double rankine = objT.determinaRankine();
        double kelvin = objT.determinaKelvin();
        
        
        
        modRes.addElement("** RESUMEN DE TEMPERATURA **");
        modRes.addElement("----------------------");
        modRes.addElement("TEMPERATURA CELSIUS: " + String.format("%.2f", objT.getCelsius()));
        modRes.addElement("----------------------");
        modRes.addElement("TEMPERATURA FARENHEIT: " + String.format("%.2f", farenheit));
        modRes.addElement("TEMPERATURA RANKINE: " + String.format("%.2f", rankine));
        modRes.addElement("TEMPERATURA KELVIN: " + String.format("%.2f", kelvin));
        
        lstResumen.setModel(modRes);
        
    }//GEN-LAST:event_btnProcesarActionPerformed

