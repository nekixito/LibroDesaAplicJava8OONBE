    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try {
            String codigo = txtCodigo.getText();
            String descripcion = txtDescripcion.getText();
            String categoria = String.valueOf(cboCategoria.getSelectedItem());
            int stock = Integer.parseInt(txtStock.getText());
            double precio = Double.parseDouble(txtPrecio.getText());
            
            Producto objProducto = new Producto(codigo, descripcion, categoria, stock, precio);
            
            ArregloProductos arreProduc = new ArregloProductos();
            
            arreProduc.adicionar(objProducto);
            
            arreProduc.grabar();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Se produjo un error: " + e);
        }
    }//GEN-LAST:event_btnGrabarActionPerformed


